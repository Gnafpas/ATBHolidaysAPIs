public class HotelXData {

    private Object destinations;
    private Object rooms;
    private Object boards;
    private Object hotels;
    private Search search;

    public Search getSearch() {
        return search;
    }

    public void setSearch(Search search) {
        this.search = search;
    }

    public Object getHotels() {
        return hotels;
    }

    public void setHotels(Object hotels) {
        this.hotels = hotels;
    }

    public Object getRooms() {
        return rooms;
    }

    public void setRooms(Object rooms) {
        this.rooms = rooms;
    }

    public Object getBoards() {
        return boards;
    }

    public void setBoards(Object boards) {
        this.boards = boards;
    }

    public Object getDestinations() {
        return destinations;
    }

    public void setDestinations(Object destinations) {
        this.destinations = destinations;
    }
}
